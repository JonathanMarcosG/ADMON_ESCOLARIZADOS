package Beans;



/**
 *
 * @author Rocio
 */
public class EscuelaProcedencia {
    
    private String idAspirante;
    private String clasificacion;
    private String estadoFK;
    private String nomEstado;
    private String nomMpio;
    private String claveEscuela;
    private String tipoEscuela;
    private String fechaIni;
    private String fechaFin;
    private String promedio;
    

    /**
     * @return the idAspirante
     */
    public String getIdAspirante() {
        return idAspirante;
    }

    /**
     * @param idAspirante the idAspirante to set
     */
    public void setIdAspirante(String idAspirante) {
        this.idAspirante = idAspirante;
    }

   

    /**
     * @return the estadoFK
     */
    public String getEstadoFK() {
        return estadoFK;
    }

    /**
     * @param estadoFK the estadoFK to set
     */
    public void setEstadoFK(String estadoFK) {
        this.estadoFK = estadoFK;
    }

    /**
     * @return the nomEstado
     */
    public String getNomEstado() {
        return nomEstado;
    }

    /**
     * @param nomEstado the nomEstado to set
     */
    public void setNomEstado(String nomEstado) {
        this.nomEstado = nomEstado;
    }

    /**
     * @return the claveEscuela
     */
    public String getClaveEscuela() {
        return claveEscuela;
    }

    /**
     * @param claveEscuela the claveEscuela to set
     */
    public void setClaveEscuela(String claveEscuela) {
        this.claveEscuela = claveEscuela;
    }

    /**
     * @return the tipoEscuela
     */
    public String getTipoEscuela() {
        return tipoEscuela;
    }

    /**
     * @param tipoEscuela the tipoEscuela to set
     */
    public void setTipoEscuela(String tipoEscuela) {
        this.tipoEscuela = tipoEscuela;
    }

    /**
     * @return the fechaIni
     */
    public String getFechaIni() {
        return fechaIni;
    }

    /**
     * @param fechaIni the fechaIni to set
     */
    public void setFechaIni(String fechaIni) {
        this.fechaIni = fechaIni;
    }

    /**
     * @return the fechaFin
     */
    public String getFechaFin() {
        return fechaFin;
    }

    /**
     * @param fechaFin the fechaFin to set
     */
    public void setFechaFin(String fechaFin) {
        this.fechaFin = fechaFin;
    }

    /**
     * @return the promedio
     */
    public String getPromedio() {
        return promedio;
    }

    /**
     * @param promedio the promedio to set
     */
    public void setPromedio(String promedio) {
        this.promedio = promedio;
    }

    /**
     * @return the clasificacion
     */
    public String getClasificacion() {
        return clasificacion;
    }

    /**
     * @param clasificacion the clasificacion to set
     */
    public void setClasificacion(String clasificacion) {
        this.clasificacion = clasificacion;
    }
    
    public String getNomMpio() {
        return nomMpio;
    }

    public void setNomMpio(String nomMpio) {
        this.nomMpio = nomMpio;
    }
    
    
   
}
